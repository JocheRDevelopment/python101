"""
Variables are essential in any programming language. 
They're like containers for storing data that can be used and manipulated throughout your code.
Python is dynamically-typed, which means you don't have to explicitly declare the type of variable before using it.
"""

#You can create a variable by simply assigning it a value with the = operator. For example:
my_number = 10
my_string = "Hello, Python!"

"""
Data Types
The main types of data in Python are:

Integers (int): Whole numbers like 3, 300, 200.
Floating-point numbers (float): Numbers with a decimal point like 2.3, 4.6, 100.0.
Strings (str): Ordered sequences of characters like "hello", "Python 101", "123".
Booleans (bool): Represents True or False.
"""

# Variables and Types in Python

# Integer
my_integer = 50
print("Integer:", my_integer)

# Float
my_float = 20.5
print("Float:", my_float)

# String
my_string = "Python is fun!"
print("String:", my_string)

# Boolean
my_boolean = True
print("Boolean:", my_boolean)

# Type checking
print("Type of my_integer:", type(my_integer))
print("Type of my_float:", type(my_float))
print("Type of my_string:", type(my_string))
print("Type of my_boolean:", type(my_boolean))

# Type conversion
my_float_to_int = int(my_float)
print("Float converted to Integer:", my_float_to_int)

my_int_to_float = float(my_integer)
print("Integer converted to Float:", my_int_to_float)

my_int_to_string = str(my_integer)
print("Integer converted to String:", my_int_to_string)





